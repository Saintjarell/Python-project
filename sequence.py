# List
list1 = ["Apple", 5, "mango", 10.6]
print(list1)
print(type(list1))
print(list1[2])

print(list1[0:2])
print(list1*3)

# Tuples
mytyple = ("mary", 15, "john")

print(mytyple)
print(mytyple[2])
print(mytyple*2)
print(type(mytyple))

# Dictionaries
devices = {1: "laptop", 2: "phone"}
print(type(devices))
print(devices.keys())
print(devices.values())

